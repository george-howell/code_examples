/**
 * @brief 	basic assert functions
 */
void assert_basic(void)
{
	int a = 1;

	assert(a != 0);

	a = 0;

	// this will assert if the outcome is false
	assert(a != 0);
}

/**
 * @brief 	assert if pointer is NULL
 */
void assert_pointer(void)
{
	int *a = malloc(sizeof(int));

	assert(a != NULL);

	int *b = NULL;

	assert(b != NULL);
}
